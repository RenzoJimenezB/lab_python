"""Propiedades de string"""

"""String Methods"""

"""Convert to uppercase: .upper()"""
my_string_1 = "Machine Learning with Python"
print(my_string_1.upper())

"""Convert to lowercase: .lower()"""
my_string_2 = "Data Analysis with Pandas"
print(my_string_2.lower())

"""Convert to title: .title()"""
my_string_3 = my_string_1 + " & " + my_string_2
print(my_string_3.title())
