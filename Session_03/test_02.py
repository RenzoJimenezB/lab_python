"""Listas en Python"""

"""pop(): Remover un elemento en una posición dada"""

paises = ["Perú", "Canadá", "Argentina", "México"]
print(f"La lista 'paises' es la siguiente: {paises}")

paises.pop()
print(f"La lista 'paises' actualizada es la siguiente: {paises}")

paises.pop(1)
print(f"La lista 'paises' actualizada es: {paises}")


