"""Listas en Python"""

"""len(): Obtener el tamaño de una lista"""

paises = []
print(f"El tamaño de la lista 'paises' es: {len(paises)}")

"""append(): Agregar datos a una lista"""

paises.append("Perú")
paises.append("España")
paises.append("Colombia")
print(f"El tamaño de la lista 'paises' es: {len(paises)}")
print(f"Los valores de la lista 'paises' son: {paises}")
