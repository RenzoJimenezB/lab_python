"""Listas en Python"""

"remove(): Elimina un elemento de la lista indicando el valor"

var_1 = ["Python", "Java", "PHP", "Javascript", "TypeScript"]
print(f"Los valores de la lista 'var_1' son: {var_1}")

var_1.remove("PHP")
var_1.remove("Java")
print(f"Los valores de la lista actualizada son: {var_1}")